package dev.java.db.model;

public class Skill extends Entity {
    private String name = "";

    public Skill() {
    }

    public Skill(long id, String name) {
        super(id);
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Skill{" +
                "name='" + name + '\'' +
                '}';
    }
}
